// Execute uma função chamada passandoPelosPares() 
// que mostra pelo console aqui eu tenho o valor de x para cada valor PAR que percorra i de 0 até e inclusive 6.

function passandoPelosPares(){
    for (var i=0; i<=6; i+=2){
        console.log("aqui eu tenho o valor de "+i);
    }
}
console.log(passandoPelosPares());
